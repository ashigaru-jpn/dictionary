package jp.mobs.gogo.android.dictionary.model;

public class TagDefModel {
    private final int id;
    private String name;

    public TagDefModel(final int id, final String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
